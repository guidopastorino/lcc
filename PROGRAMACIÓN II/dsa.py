# LINKED LISTS

# Linked Lists are a type of dynamic and linear data structure. These form a set of nodes connected through pointers. Each node contains its data and the address/reference to the next node. The last node contains a "null" reference to a next node. 

# HEAD -> [DATA, NEXT] -> [DATA, NEXT] -> [DATA, NEXT] -> NULL
#               ^                ^               ^
#              node             node            node


# NODE structure: It typically consists of two components:
    # - Data: It holds the actual value or data associated with the node. 
    # - Next Pointer: It stores the memory address (reference) of the next node in the sequence. 